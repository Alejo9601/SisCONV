    /**
     * Will set the model for the table.
     *
     * @param model
     */
    public void setTableModel(DefaultTableModel model) {
        DefaultTableCellRenderer tcr = new DefaultTableCellRenderer();
        tcr.setHorizontalAlignment(SwingConstants.CENTER); //CENTER o LEFT
        tblLandProperty.setModel(model);  //Setting model
        tblLandProperty.setRowHeight(25); //Setting rows height
        tblLandProperty.setFont(new java.awt.Font("Tahoma", 0, 18)); //Changing font
        tblLandProperty.getTableHeader().setReorderingAllowed(false); //Disabling reordering columns
        for (int i = 0; i < tblLandProperty.getColumnCount(); i++) {
            tblLandProperty.getColumnModel().getColumn(i).setCellRenderer(tcr);
        }
        //Making first column not visible for users.
        tblLandProperty.getColumn(tblLandProperty.getColumnName(0)).setWidth(0);
        tblLandProperty.getColumn(tblLandProperty.getColumnName(0)).setMinWidth(0);
        tblLandProperty.getColumn(tblLandProperty.getColumnName(0)).setMaxWidth(0);
    }


    /**
     * Gets the information of the selected row.
     *
     * @return
     */
    public String getSelectedRecord() {
        String chain = "";
        for (int i = 0; i < tblLandProperty.getColumnCount(); i++) {
            if (i == tblLandProperty.getColumnCount() - 1) {
                chain += (String) tblLandProperty.getValueAt(tblLandProperty.getSelectedRow(), i);
            } else {
                chain += (String) tblLandProperty.getValueAt(tblLandProperty.getSelectedRow(), i) + " : ";
            }
        }
        return chain;
    }


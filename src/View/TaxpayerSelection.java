    /**
     * Will set the model for the table.
     *
     * @param model
     */
    public void setTableModel(DefaultTableModel model) {
        DefaultTableCellRenderer tcr = new DefaultTableCellRenderer();
        tcr.setHorizontalAlignment(SwingConstants.CENTER); //CENTER o LEFT
        tblTaxPayers.setModel(model);  //Setting model
        tblTaxPayers.setRowHeight(25); //Setting rows height
        tblTaxPayers.setFont(new java.awt.Font("Tahoma", 0, 18)); //Changing font
        tblTaxPayers.getTableHeader().setReorderingAllowed(false); //Disabling reordering columns
        for (int i = 0; i < tblTaxPayers.getColumnCount(); i++) {
            tblTaxPayers.getColumnModel().getColumn(i).setCellRenderer(tcr);
        }
    }


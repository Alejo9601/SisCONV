    /**
     * Gets the information of the selected row.
     *
     * @return
     */
    public String getSelectedRecord() {
        String chain = "";
        for (int i = 0; i < tblTaxPayers.getColumnCount(); i++) {
            if (i == tblTaxPayers.getColumnCount() - 1) {
                chain += (String) tblTaxPayers.getValueAt(tblTaxPayers.getSelectedRow(), i);
            } else {
                chain += (String) tblTaxPayers.getValueAt(tblTaxPayers.getSelectedRow(), i) + " : ";
            }
        }
        return chain;
    }

    /**
     * Will filter taxpayers table.
     */
    public void filterTaxpayers() {
        TableRowSorter trsFiltro = new TableRowSorter(tblTaxPayers.getModel());
        if (chkDocNumber.isSelected() || chkLastname.isSelected()) {
            if (chkDocNumber.isSelected()) {
                trsFiltro.setRowFilter(RowFilter.regexFilter(tfSearch.getText().toUpperCase(), 0));
            } else {
                trsFiltro.setRowFilter(RowFilter.regexFilter(tfSearch.getText().toUpperCase(), 2));
            }
        }
        tblTaxPayers.setRowSorter(trsFiltro);
    }


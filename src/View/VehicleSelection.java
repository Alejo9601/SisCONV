    /**
     * Gets the information of the selected row.
     *
     * @return
     */
    public String getSelectedRecord() {
        String chain = "";
        for (int i = 0; i < tblVehicle.getColumnCount(); i++) {
            if (i == tblVehicle.getColumnCount() - 1) {
                chain += (String) tblVehicle.getValueAt(tblVehicle.getSelectedRow(), i);
            } else {
                chain += (String) tblVehicle.getValueAt(tblVehicle.getSelectedRow(), i) + " : ";
            }
        }
        return chain;
    }

    /**
     * Will filter vehicle table.
     */
    public void filterVehicles() {
        TableRowSorter trsFiltro = new TableRowSorter(tblVehicle.getModel());
        if (chkDomain.isSelected() || chkModel.isSelected()) {
            if (chkDomain.isSelected()) {
                trsFiltro.setRowFilter(RowFilter.regexFilter(tfSearch.getText().toUpperCase(), 1));
            } else {
                trsFiltro.setRowFilter(RowFilter.regexFilter(tfSearch.getText().toUpperCase(), 2));
            }
        }
        tblVehicle.setRowSorter(trsFiltro);
    }


    /**
     * Will set the model for the table.
     *
     * @param model
     */
    public void setTableModel(DefaultTableModel model) {
        DefaultTableCellRenderer tcr = new DefaultTableCellRenderer();
        tcr.setHorizontalAlignment(SwingConstants.CENTER); //CENTER o LEFT
        tblVehicle.setModel(model);  //Setting model
        tblVehicle.setRowHeight(25); //Setting rows height
        tblVehicle.setFont(new java.awt.Font("Tahoma", 0, 18)); //Changing font
        tblVehicle.getTableHeader().setReorderingAllowed(false); //Disabling reordering columns
        for (int i = 0; i < tblVehicle.getColumnCount(); i++) {
            tblVehicle.getColumnModel().getColumn(i).setCellRenderer(tcr);
        }
        //Making first column not visible for users.
        tblVehicle.getColumn(tblVehicle.getColumnName(0)).setWidth(0);
        tblVehicle.getColumn(tblVehicle.getColumnName(0)).setMinWidth(0);
        tblVehicle.getColumn(tblVehicle.getColumnName(0)).setMaxWidth(0);
    }

